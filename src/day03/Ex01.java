package day03;

public class Ex01 {
	public static void main(String[] args) {

		/*   
		 
		  /(나누기) : 정수/정수 = 정수(몫)
		    - 실수/정수 = 실수
		  %(mod) : 정수%정수 = 나머지 
		% 연산자를 이용하여 어떤수의 배수 또는 짝,홀을 구분
		
		10(N) % 2 = 0 : 짝수
		10(N) % 2 = 1 : 홀수
		
		12(N) % 2 = 0 : 짝수이면서 2의 배수
		12(N) % 3 = 0 : 3의 배수
		   */

		int n1 = 9, n2 = 2;
		System.out.println(n1*n2);
		System.out.println(n1/n2);
		System.out.println(n1%n2);
		
		
		
		
		
		
	}
}
